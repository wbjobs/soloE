export function hexToColorIndex(hex) {
  const index = COLORS.indexOf(hex.toLowerCase())
  return index >= 0 ? index + 1 : 1
}

export function colorIndexToHex(index) {
  if (index <= 0 || index > COLORS.length) return '#ffffff'
  return COLORS[index - 1]
}

