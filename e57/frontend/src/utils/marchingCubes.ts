function interpolate(v0: number, v1: number, t: number): number {
  const d = v1 - v0;
  if (Math.abs(d) < 0.00001) return 0;
  return (t - v0) / d;
}

function getEdgeVertices(i: number): [number, number] {
  const edges: [number, number][] = [
    [0, 1], [1, 2], [2, 3], [3, 0],
    [4, 5], [5, 6], [6, 7], [7, 4],
    [0, 4], [1, 5], [2, 6], [3, 7]
  ];
  return edges[i];
}

